package arrayListLearning;

import java.util.HashMap;
import java.util.Map;

public class Maps {

	public static void main(String[] args) {
		// works withKey, Value pair
		// Map: works same as set
		// values can be duplicated but keys cannot
		Map<String, String> words = new HashMap<String, String>();
		words.put("a", "zini");
		words.put("b", "sun");
		words.put("c", "tanvir");
		words.put("d", "nawaz");
		words.put("e", "akther");
		System.out.println("");
	}

}
